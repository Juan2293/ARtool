package co.edu.usbcali.modelo;

public class Funcion {
	
	private String nombre_funcion;
	private String nombre_tabla;
	private String nombre_relacion;
	
	
	public String getNombre_tabla() {
		return nombre_tabla;
	}
	public void setNombre_tabla(String nombre_tabla) {
		this.nombre_tabla = nombre_tabla;
	}
	public String getNombre_funcion() {
		return nombre_funcion;
	}
	public void setNombre_funcion(String nombre_funcion) {
		this.nombre_funcion = nombre_funcion;
	}
	public String getNombre_relacion() {
		return nombre_relacion;
	}
	public void setNombre_relacion(String nombre_relacion) {
		this.nombre_relacion = nombre_relacion;
	}

	
}
